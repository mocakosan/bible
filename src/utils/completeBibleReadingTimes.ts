/**
 * 통합된 성경 전체 읽기 시간 데이터
 */
const ALL_BIBLE_READING_TIMES = {
    ...OLD_TESTAMENT_READING_TIMES,
    ...NEW_TESTAMENT_READING_TIMES
};

/**
 * 특정 성경책의 특정 장 읽기 시간 반환
 */
export const getChapterReadingTime = (book: number, chapter: number): number => {
    const bookData = ALL_BIBLE_READING_TIMES[book];
    if (bookData && bookData[chapter]) {
        return bookData[chapter];
    }

    // fallback: 기본 추정치
    if (book === 19) return 2.5;      // 시편
    if (book === 20) return 3.5;      // 잠언
    if (book <= 39) return 4.0;       // 구약
    return 4.2;                       // 신약
};

/**
 * 특정 성경책의 전체 읽기 시간 계산
 */
export const getBookTotalTime = (book: number): number => {
    const bookData = ALL_BIBLE_READING_TIMES[book];
    if (!bookData) return 0;

    return Object.values(bookData).reduce((total, time) => total + time, 0);
};

/**
 * 성경 계획별 총 읽기 시간 계산
 */
export const calculatePlanTotalTime = (planType: string): number => {
    let startBook = 1;
    let endBook = 66;

    switch (planType) {
        case 'old_testament':
            startBook = 1;
            endBook = 39;
            break;
        case 'new_testament':
            startBook = 40;
            endBook = 66;
            break;
        case 'pentateuch':
            startBook = 1;
            endBook = 5;
            break;
        case 'psalms':
            startBook = 19;
            endBook = 19;
            break;
        case 'full_bible':
        default:
            startBook = 1;
            endBook = 66;
            break;
    }

    let totalTime = 0;
    for (let book = startBook; book <= endBook; book++) {
        totalTime += getBookTotalTime(book);
    }

    return totalTime;
};

/**
 * 성경 계획별 총 장수 계산
 */
export const calculatePlanTotalChapters = (planType: string): number => {
    let startBook = 1;
    let endBook = 66;

    switch (planType) {
        case 'old_testament':
            startBook = 1;
            endBook = 39;
            break;
        case 'new_testament':
            startBook = 40;
            endBook = 66;
            break;
        case 'pentateuch':
            startBook = 1;
            endBook = 5;
            break;
        case 'psalms':
            startBook = 19;
            endBook = 19;
            break;
        case 'full_bible':
        default:
            startBook = 1;
            endBook = 66;
            break;
    }

    let totalChapters = 0;
    for (let book = startBook; book <= endBook; book++) {
        const bookData = ALL_BIBLE_READING_TIMES[book];
        if (bookData) {
            totalChapters += Object.keys(bookData).length;
        }
    }

    return totalChapters;
};

/**
 * 시간 기반으로 하루 읽을 장수 계산 (정확한 시간 적용)
 */
export const calculateChaptersFromTimeGoal = (
    planType: string,
    targetMinutesPerDay: number
): number => {
    const totalTime = calculatePlanTotalTime(planType);
    const totalChapters = calculatePlanTotalChapters(planType);

    if (totalChapters === 0) {
        return Math.max(1, Math.round(targetMinutesPerDay / 4));
    }

    const avgTimePerChapter = totalTime / totalChapters;
    const chaptersForTargetTime = Math.round(targetMinutesPerDay / avgTimePerChapter);

    return Math.max(1, Math.min(chaptersForTargetTime, 15));
};

/**
 * 최적화된 일독 계획 생성 (모든 성경에 적용)
 */
export const createOptimizedBiblePlan = (
    planType: string,
    startDate: string,
    endDate: string,
    targetDays?: number
): any => {
    const start = new Date(startDate);
    const end = new Date(endDate);
    const totalDays = targetDays || Math.ceil((end.getTime() - start.getTime()) / (1000 * 60 * 60 * 24)) + 1;

    const totalTime = calculatePlanTotalTime(planType);
    const totalChapters = calculatePlanTotalChapters(planType);
    const recommendedTimePerDay = Math.round((totalTime / totalDays) * 10) / 10;

    // 하루 평균 장수 계산
    const avgChaptersPerDay = Math.ceil(totalChapters / totalDays);

    const planNames: { [key: string]: string } = {
        'full_bible': '성경',
        'old_testament': '구약',
        'new_testament': '신약',
        'pentateuch': '모세오경',
        'psalms': '시편'
    };

    return {
        planType,
        planName: planNames[planType] || '성경',
        startDate,
        endDate,
        totalDays,
        totalChapters,

        // 정확한 계산값
        chaptersPerDay: avgChaptersPerDay,
        calculatedMinutesPerDay: recommendedTimePerDay,

        // 실제 시간 데이터
        totalMinutes: Math.round(totalTime),

        isTimeBasedCalculation: true,
        currentDay: 1,
        readChapters: [],
        createdAt: new Date().toISOString(),
        version: '3.0_complete_bible_optimized'
    };
};

